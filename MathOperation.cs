namespace SimpleCalculator
{
    public class MathOperation {

        public double Addition(int x, int y) {
            return x + y;
        }

        public double Subtraction(int x, int y) {
            return x - y;
        }

        public double Multiplication(int x, int y) {
            return x * y;
        }

        public double Division(int x, int y) {
            return x / y;
        }
    }
}